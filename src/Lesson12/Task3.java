package Lesson12;

public class Task3 {
    static void main() {
        int x = 5;
        System.out.println(x);

        x++;
        System.out.println(x);

        ++x;
        System.out.println(x);

        x--;
        System.out.println(x);

        --x;
        System.out.println(x);
    }
}
